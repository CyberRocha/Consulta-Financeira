CREATE TABLE IF NOT EXISTS [MovimentoContaBancaria](
  [ID_Cliente] INT NOT NULL, 
  [Banco] VARCHAR(10) NOT NULL, 
  [Conta] VARCHAR(50) NOT NULL, 
  [ID_Documento] VARCHAR(50) NOT NULL, 
  [DT_Movimento] DATETIME NOT NULL, 
  [VL_Movimento] CURRENCY NOT NULL, 
  [Flag_Movimento] CHAR(1) NOT NULL, 
  [Descricao] VARCHAR(80) NOT NULL, 
  PRIMARY KEY([ID_Cliente] COLLATE [NOCASE] ASC, [Banco] COLLATE [NOCASE] ASC, [Conta] COLLATE [NOCASE] ASC, [ID_Documento] COLLATE [NOCASE] ASC, [DT_Movimento] COLLATE [NOCASE] ASC) ON CONFLICT ROLLBACK);
CREATE TABLE IF NOT EXISTS [CotacaoMoeda](
  [ID_Moeda] CHAR(3) NOT NULL, 
  [DT_Cotacao] DATETIME NOT NULL, 
  [VL_Compra] CURRENCY NOT NULL, 
  [VL_Venda] CURRENCY NOT NULL, 
  [VL_Variacao] CURRENCY NOT NULL, 
  PRIMARY KEY([ID_Moeda] COLLATE [NOCASE] ASC, [DT_Cotacao] COLLATE [NOCASE] ASC) ON CONFLICT ROLLBACK);
CREATE TABLE IF NOT EXISTS [ContaBancaria](
  [ID_Cliente] INT NOT NULL, 
  [Banco] VARCHAR(10) NOT NULL, 
  [Conta] VARCHAR(50) NOT NULL, 
  [VL_Saldo] CURRENCY NOT NULL, 
  [DT_Saldo] DATETIME NOT NULL, 
  PRIMARY KEY([ID_Cliente] ASC, [Banco] COLLATE [NOCASE] ASC, [Conta] COLLATE [NOCASE] ASC) ON CONFLICT ROLLBACK);
CREATE TABLE IF NOT EXISTS [Clientes](
  [ID_Cliente] Integer NOT NULL, 
  [Nome_Cliente] VARCHAR(50), 
  PRIMARY KEY([ID_Cliente] COLLATE [NOCASE] ASC) ON CONFLICT ROLLBACK);